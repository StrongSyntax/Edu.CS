let askedQuestions = [];
let currentScore = 0;
let totalQuestions = questions.length;

function getRandomQuestion() {
    if (questions.length === 0) {
        displayFinalScore();
        return null;
    }
    let randomIndex = Math.floor(Math.random() * questions.length);
    let question = questions[randomIndex];
    questions.splice(randomIndex, 1); // Remove the question so it's not asked again
    askedQuestions.push(question);
    return question;
}

function showQuestion() {
    let question = getRandomQuestion();
    if (question) {
        document.getElementById('question').innerText = question.question;
        document.getElementById('correctAnswer').innerText = '';
        document.getElementById('userAnswer').value = '';
    }
}

function showAnswer() {
    let questionText = document.getElementById('question').innerText;
    let answer = askedQuestions.find(q => q.question === questionText).answer;
    document.getElementById('correctAnswer').innerText = answer;
}

function updateScore(points) {
    currentScore += points;
    document.getElementById('score').innerText = currentScore;
    showQuestion(); // Show next question
}

function displayFinalScore() {
    let finalScore = (currentScore / totalQuestions) * 100;
    document.getElementById('finalScore').innerText = `Final Score: ${finalScore.toFixed(2)}%`;
    document.getElementById('quizContainer').style.display = 'none';
}

window.onload = showQuestion;
